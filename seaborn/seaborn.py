"""### 1-VAZIFA. Avvalgi [amaliyotda](https://colab.research.google.com/drive/1jzhJ1gN6msILYOHaG2fhBBvUGaa4tcLf?usp=sharing) saqlangan faylni yangi `df`ga o'qing"""

#JAVOBINGIZNI SHU YERGA YOZING
df = pd.read_csv("tashtemp.csv", index_col=0)
df.head()

"""### 2-VAZIFA. `df` barcha ustunlarini chiziqli grafik ko'rinishida chiqaring"""

#JAVOBINGIZNI SHU YERGA YOZING
plt.figure(figsize=(16,6))
